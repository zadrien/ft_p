#include "ftp.h"

int     ft_cwd(t_token **lst, int s)
{
    char    buf[4] = "CWD\0";

    if (send_cmd(buf, &(*lst)->next, s))
        if (get_code(s) == 250)
            return (1);
    return (0);
}